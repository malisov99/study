import collections

#Создаём структуру данных
pets = {
    1: {
        "Мухтар": {
            "Вид питомца": "Собака",
            "Возраст питомца": 9,
            "Имя владельца": "Павел"
        }
    },
    2: {
        "Каа": {
            "Вид питомца": "желторотый питон",
            "Возраст питомца": 19,
            "Имя владельца": "Саша"
        }
    }
}

#Вспомогательная функция
def get_pet(ID):
    return pets[ID] if ID in pets else False

#Функция для правильного склонения
def get_suffix(age):
    if 11 <= age % 100 <= 14:
        return "лет"
    elif age % 10 == 1:
        return "год"
    elif 2 <= age % 10 <= 4:
        return "года"
    else:
        return "лет"

#Функция create
def create():
    if pets:
        last = collections.deque(pets, maxlen=1)[0]
        new_id = last + 1
    else:
        new_id = 1

    name = input("Введите кличку питомца: ")
    species = input("Введите вид питомца: ")
    age = int(input("Введите возраст питомца: "))
    owner = input("Введите имя владельца: ")

    pets[new_id] = {
        name: {
            "Вид питомца": species,
            "Возраст питомца": age,
            "Имя владельца": owner
        }
    }

    print("Питомец успешно добавлен с ID:", new_id)

#Функция read
def read():
    ID = int(input("Введите ID питомца: "))
    pet = get_pet(ID)

    if not pet:
        print("Питомца с таким ID нет")
        return

    for name, info in pet.items():
        age = info["Возраст питомца"]
        print(
            f'Это {info["Вид питомца"]} по кличке "{name}". '
            f'Возраст питомца: {age} {get_suffix(age)}. '
            f'Имя владельца: {info["Имя владельца"]}'
        )

#Функция update
def update():
    ID = int(input("Введите ID питомца: "))
    pet = get_pet(ID)

    if not pet:
        print("Питомца с таким ID нет")
        return

    for name in pet:
        species = input("Введите новый вид питомца: ")
        age = int(input("Введите новый возраст питомца: "))
        owner = input("Введите новое имя владельца: ")

        pets[ID][name] = {
            "Вид питомца": species,
            "Возраст питомца": age,
            "Имя владельца": owner
        }

    print("Информация обновлена")

#Функция delete
def delete():
    ID = int(input("Введите ID питомца: "))

    if ID in pets:
        del pets[ID]
        print("Питомец удалён")
    else:
        print("Питомца с таким ID нет")

#Функция вывода списка всех питомцев
def pets_list():
    if not pets:
        print("База данных пуста")
        return

    for ID, pet in pets.items():
        for name in pet:
            print(f"ID: {ID}, Кличка: {name}")

#Главный цикл программы
while True:
    command = input(
        "\nВведите команду (create, read, update, delete, list, stop): "
    ).lower()

    if command == "create":
        create()
    elif command == "read":
        read()
    elif command == "update":
        update()
    elif command == "delete":
        delete()
    elif command == "list":
        pets_list()
    elif command == "stop":
        print("Работа завершена")
        break
    else:
        print("Неизвестная команда")